import { readFileSync, writeFileSync } from 'fs';

const kbPath = new URL('../documentation/website-chatbot-knowledge-base.md', import.meta.url);
const outPath = new URL('../netlify/functions/lib/chatbotKnowledgeBase.generated.ts', import.meta.url);

const kbContent = readFileSync(kbPath, 'utf8');

// Baked into the function bundle (via JSON.stringify, not a template
// literal, so nothing in the markdown - backticks, ${...}, etc. - can break
// out of the string) rather than read from disk at runtime, since Netlify's
// function bundler doesn't reliably ship arbitrary non-JS files alongside a
// function.
//
// This MUST be run manually, not wired into `build`/`prebuild`: the source
// markdown lives under /documentation/, which is entirely .gitignore'd for
// this project (same reason PDF_CATALOG.md's own sync script is manual-only
// too - see catalog:sync) - on Netlify's build server that file doesn't
// exist, so a build-time dependency on it would fail the whole site's
// deploy, not just the chatbot. The generated output below is NOT
// gitignored and IS the actual deployed source of truth - after editing
// documentation/website-chatbot-knowledge-base.md, run
// `npm run chatbot-kb:sync` and commit the regenerated file.
const output = `// AUTO-GENERATED by scripts/generate-chatbot-kb.mjs from
// documentation/website-chatbot-knowledge-base.md - do not hand-edit.
// Re-run \`npm run chatbot-kb:sync\` after editing the source markdown.

export const CHATBOT_KNOWLEDGE_BASE = ${JSON.stringify(kbContent)};
`;

writeFileSync(outPath, output);
console.log(`chatbotKnowledgeBase.generated.ts regenerated (${kbContent.length} chars).`);
