import { PRODUCTS } from '../src/constants.ts';
import { writeFileSync } from 'fs';

// Only products a buyer could actually open matter for chatbot navigation -
// a pre-order/coming-soon item has nothing to jump to yet.
const index = PRODUCTS.filter((p) => p.available !== false).map((p) => ({
  id: p.id,
  title: p.title,
  category: p.category
}));

const output = `// AUTO-GENERATED by scripts/generate-chatbot-product-index.mjs from
// src/constants.ts - do not hand-edit. Re-run \`npm run chatbot-index:sync\`
// after changing PRODUCTS in src/constants.ts, then commit the regenerated
// file.
//
// This exists so netlify/functions/chatbot.ts has a plain, Vite-free copy
// of the catalog to validate/constrain the model's product matching
// against - it can't import src/constants.ts directly (that file reads
// import.meta.env.VITE_IMAGE_BASE_URL, a Vite-only construct not available
// in the Netlify Functions runtime).

export interface ChatbotProductIndexEntry {
  id: string;
  title: string;
  category: string;
}

export const CHATBOT_PRODUCT_INDEX: ChatbotProductIndexEntry[] = ${JSON.stringify(index, null, 2)};
`;

writeFileSync(new URL('../netlify/functions/lib/chatbotProductIndex.generated.ts', import.meta.url), output);
console.log(`chatbotProductIndex.generated.ts regenerated (${index.length} products).`);
